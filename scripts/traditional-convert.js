// Sort phrases longest first, then append single chars
REPLACEMENTS.sort(function(a, b) { return b[0].length - a[0].length })
var ALL = REPLACEMENTS.concat(CHARS)

const files = execSync(
  'find ' + ROOT + '/apps/web/src -name "*.tsx" -o -name "*.ts" | grep -v node_modules | grep -v ".next"',
  { encoding: 'utf8' }
).trim().split('\n').filter(Boolean)

var totalChanges = 0

for (var fi = 0; fi < files.length; fi++) {
  var f = files[fi]
  var rel = path.relative(ROOT, f)
  var content = fs.readFileSync(f, 'utf8')
  var changes = 0

  for (var ri = 0; ri < ALL.length; ri++) {
    var from = ALL[ri][0]
    var to = ALL[ri][1]
    if (from === to) continue
    var count = (content.match(new RegExp(from.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g')) || []).length
    if (count > 0) {
      content = content.split(from).join(to)
      changes += count
    }
  }

  if (changes > 0) {
    fs.writeFileSync(f, content, 'utf8')
    totalChanges += changes
    console.log('[OK] ' + rel + ': ' + changes)
  }
}

console.log('\nDone: ' + totalChanges + ' replacements')
